<?php
   abstract class Calc {
      
      public function multi($a,$b) {
         
         return $a*$b;
      }
      public function minus($a,$b){
         
         return $a-$b;
      }
        public function plus($a,$b){
         
         return $a+$b;
      }
        public function divide($a,$b){
         
         return $a/$b;
      }

   }
   ?>




